
function openCity(city) {
    if (city === 'londra') {
        window.location.href = 'https://www.visitlondon.com/';
    } else if (city === 'newyork') {
        window.location.href = 'https://www.nycgo.com/';
    } else if (city === 'parigi') {
        window.location.href = 'https://en.parisinfo.com/';
    }
}
